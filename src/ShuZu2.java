public class ShuZu2 {
    public static void main(String[] args) {
        // 定义一个整数数组，包含元素1到9
        int [] arr = {1,2,3,4,5,6,7,8,9};
        // 获取数组的第一个元素，并将其赋值给变量temp
        int temp = arr[0];
        // 打印变量temp的值
        System.out.println(temp);
        //直接打印
        int [] arr2 = {1,2,3,4,5,6,7,8,9};
        System.out.println(arr2[0]);
    }
}
